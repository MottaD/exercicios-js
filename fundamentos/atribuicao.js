const a = 7 // o símbolo de = é uma atribuição
let b = 3

b += a //+= é uma atribuição aditiva, ou seja, junto da atribuição ele está realizando adição
//b = b + a
console.log(b)

b -= 4 //-= atribuição subtrativa, ou seja, b = b - 4
console.log(b)

b *= 2 //*= atribuição multiplicativa, ou seja, b = b * 2
console.log(b)

b /= 2 // /= atribuição divisiva, ou seja, b = b /2
console.log(b)

b %= 2 // %= atribuição modular, ou seja, b = b % 2 (resto da divisão)
console.log(b)