//Operador de exponeciação:
/*Representado por dois asteriscos (**) o operador nos trouxe outra possibilidade
interessante que podemos chamar de atribuição exponencial */

let exp = 2 ** 3 //O primeiro valor representa a base e o segundo o expoente
console.log(exp) //2 elevado a 3 é igual a 8

