//Valores Unarios

let num1 = 1
let num2 = 2

num1++ //forma pós fixado, ele acrescenta 1 unidade ao valor da unidade
console.log(num1)

--num1 //forma pré fixado, ele subtrai 1 unidade do valor da unidade
console.log(num1)

console.log(++num1 === num2--)
console.log(num1 === num2)

